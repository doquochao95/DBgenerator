import { Helper } from './xml';
var sax = require('sax');
var options: any;
var pureJsParser = true;
var currentElement: any;
let h = new Helper();

export function xml2js(xml: any, userOptions: any) {
  var parser: any = sax.parser(true, {});
  var result: any = {};
  currentElement = result;

  options = validateOptions(userOptions);

  if (pureJsParser) {
    parser.opt = { strictEntities: true };
    parser.onopentag = onStartElement;
    parser.ontext = onText;
    parser.oncomment = onComment;
    parser.onclosetag = onEndElement;
    parser.onerror = onError;
    parser.oncdata = onCdata;
    parser.ondoctype = onDoctype;
    parser.onprocessinginstruction = onInstruction;
  } else {
    parser.on('startElement', onStartElement);
    parser.on('text', onText);
    parser.on('comment', onComment);
    parser.on('endElement', onEndElement);
    parser.on('error', onError);
  }

  if (pureJsParser) {
    parser.write(xml).close();
  } else {
    if (!parser.parse(xml)) {
      throw new Error('XML parsing error: ' + parser.getError());
    }
  }

  if (result[options.elementsKey]) {
    var temp = result[options.elementsKey];
    delete result[options.elementsKey];
    result[options.elementsKey] = temp;
    delete result.text;
  }

  return result;
}
function validateOptions(userOptions: any) {
  options = h.copyOptions(userOptions);
  h.ensureFlagExists('ignoreDeclaration', options);
  h.ensureFlagExists('ignoreInstruction', options);
  h.ensureFlagExists('ignoreAttributes', options);
  h.ensureFlagExists('ignoreText', options);
  h.ensureFlagExists('ignoreComment', options);
  h.ensureFlagExists('ignoreCdata', options);
  h.ensureFlagExists('ignoreDoctype', options);
  h.ensureFlagExists('compact', options);
  h.ensureFlagExists('alwaysChildren', options);
  h.ensureFlagExists('addParent', options);
  h.ensureFlagExists('trim', options);
  h.ensureFlagExists('nativeType', options);
  h.ensureFlagExists('nativeTypeAttributes', options);
  h.ensureFlagExists('sanitize', options);
  h.ensureFlagExists('instructionHasAttributes', options);
  h.ensureFlagExists('captureSpacesBetweenElements', options);
  h.ensureAlwaysArrayExists(options);
  h.ensureKeyExists('declaration', options);
  h.ensureKeyExists('instruction', options);
  h.ensureKeyExists('attributes', options);
  h.ensureKeyExists('text', options);
  h.ensureKeyExists('comment', options);
  h.ensureKeyExists('cdata', options);
  h.ensureKeyExists('doctype', options);
  h.ensureKeyExists('type', options);
  h.ensureKeyExists('name', options);
  h.ensureKeyExists('elements', options);
  h.ensureKeyExists('parent', options);
  h.checkFnExists('doctype', options);
  h.checkFnExists('instruction', options);
  h.checkFnExists('cdata', options);
  h.checkFnExists('comment', options);
  h.checkFnExists('text', options);
  h.checkFnExists('instructionName', options);
  h.checkFnExists('elementName', options);
  h.checkFnExists('attributeName', options);
  h.checkFnExists('attributeValue', options);
  h.checkFnExists('attributes', options);
  return options;
}

function nativeType(value: any) {
  var nValue = Number(value);
  if (!isNaN(nValue)) {
    return nValue;
  }
  var bValue = value.toLowerCase();
  if (bValue === 'true') {
    return true;
  } else if (bValue === 'false') {
    return false;
  }
  return value;
}

function addField(type: any, value: any) {
  var key: any;
  if (options.compact) {
    if (
      !currentElement[options[type + 'Key']] &&
      (h.isArray(options.alwaysArray)
        ? options.alwaysArray.indexOf(options[type + 'Key']) !== -1
        : options.alwaysArray)
    ) {
      currentElement[options[type + 'Key']] = [];
    }
    if (
      currentElement[options[type + 'Key']] &&
      !h.isArray(currentElement[options[type + 'Key']])
    ) {
      currentElement[options[type + 'Key']] = [
        currentElement[options[type + 'Key']],
      ];
    }
    if (type + 'Fn' in options && typeof value === 'string') {
      value = options[type + 'Fn'](value, currentElement);
    }
    if (
      type === 'instruction' &&
      ('instructionFn' in options || 'instructionNameFn' in options)
    ) {
      for (key in value) {
        if (value.hasOwnProperty(key)) {
          if ('instructionFn' in options) {
            value[key] = options.instructionFn(value[key], key, currentElement);
          } else {
            var temp = value[key];
            delete value[key];
            value[options.instructionNameFn(key, temp, currentElement)] = temp;
          }
        }
      }
    }
    if (h.isArray(currentElement[options[type + 'Key']])) {
      currentElement[options[type + 'Key']].push(value);
    } else {
      currentElement[options[type + 'Key']] = value;
    }
  } else {
    if (!currentElement[options.elementsKey]) {
      currentElement[options.elementsKey] = [];
    }
    var element: any = {};
    element[options.typeKey] = type;
    if (type === 'instruction') {
      for (key in value) {
        if (value.hasOwnProperty(key)) {
          break;
        }
      }
      element[options.nameKey] =
        'instructionNameFn' in options
          ? options.instructionNameFn(key, value, currentElement)
          : key;
      if (options.instructionHasAttributes) {
        element[options.attributesKey] = value[key][options.attributesKey];
        if ('instructionFn' in options) {
          element[options.attributesKey] = options.instructionFn(
            element[options.attributesKey],
            key,
            currentElement
          );
        }
      } else {
        if ('instructionFn' in options) {
          value[key] = options.instructionFn(value[key], key, currentElement);
        }
        element[options.instructionKey] = value[key];
      }
    } else {
      if (type + 'Fn' in options) {
        value = options[type + 'Fn'](value, currentElement);
      }
      element[options[type + 'Key']] = value;
    }
    if (options.addParent) {
      element[options.parentKey] = currentElement;
    }
    currentElement[options.elementsKey].push(element);
  }
}

function manipulateAttributes(attributes: any) {
  if ('attributesFn' in options && attributes) {
    attributes = options.attributesFn(attributes, currentElement);
  }
  if (
    (options.trim ||
      'attributeValueFn' in options ||
      'attributeNameFn' in options ||
      options.nativeTypeAttributes) &&
    attributes
  ) {
    var key;
    for (key in attributes) {
      if (attributes.hasOwnProperty(key)) {
        if (options.trim) {
          attributes[key] = attributes[key].trim();
        }
        if (options.nativeTypeAttributes) {
          attributes[key] = nativeType(attributes[key]);
        }
        if ('attributeValueFn' in options) {
          attributes[key] = options.attributeValueFn(
            attributes[key],
            key,
            currentElement
          );
        }
        if ('attributeNameFn' in options) {
          var temp = attributes[key];
          delete attributes[key];
          attributes[
            options.attributeNameFn(key, attributes[key], currentElement)
          ] = temp;
        }
      }
    }
  }
  return attributes;
}

function onInstruction(instruction: any) {
  var attributes: any = {};
  if (
    instruction.body &&
    (instruction.name.toLowerCase() === 'xml' ||
      options.instructionHasAttributes)
  ) {
    var attrsRegExp = /([\w:-]+)\s*=\s*(?:"([^"]*)"|'([^']*)'|(\w+))\s*/g;
    var match: any;
    while ((match = attrsRegExp.exec(instruction.body)) !== null) {
      attributes[match[1]] = match[2] || match[3] || match[4];
    }
    attributes = manipulateAttributes(attributes);
  }
  if (instruction.name.toLowerCase() === 'xml') {
    if (options.ignoreDeclaration) {
      return;
    }
    currentElement[options.declarationKey] = {};
    if (Object.keys(attributes).length) {
      currentElement[options.declarationKey][options.attributesKey] =
        attributes;
    }
    if (options.addParent) {
      currentElement[options.declarationKey][options.parentKey] =
        currentElement;
    }
  } else {
    if (options.ignoreInstruction) {
      return;
    }
    if (options.trim) {
      instruction.body = instruction.body.trim();
    }
    var value: any = {};
    if (options.instructionHasAttributes && Object.keys(attributes).length) {
      value[instruction.name] = {};
      value[instruction.name][options.attributesKey] = attributes;
    } else {
      value[instruction.name] = instruction.body;
    }
    addField('instruction', value);
  }
}

function onStartElement(name: any, attributes: any) {
  var element: any;
  var isSelfClosing = name?.isSelfClosing === true;
  if (typeof name === 'object') {
    attributes = name.attributes;
    name = name.name;
  }
  attributes = manipulateAttributes(attributes);
  if ('elementNameFn' in options) {
    name = options.elementNameFn(name, currentElement);
  }
  if (options.compact) {
    element = {};
    if (
      !options.ignoreAttributes &&
      attributes &&
      Object.keys(attributes).length
    ) {
      element[options.attributesKey] = {};
      var key;
      for (key in attributes) {
        if (attributes.hasOwnProperty(key)) {
          element[options.attributesKey][key] = attributes[key];
        }
      }
    }
    if (
      !(name in currentElement) &&
      (h.isArray(options.alwaysArray)
        ? options.alwaysArray.indexOf(name) !== -1
        : options.alwaysArray)
    ) {
      currentElement[name] = [];
    }
    if (currentElement[name] && !h.isArray(currentElement[name])) {
      currentElement[name] = [currentElement[name]];
    }
    if (h.isArray(currentElement[name])) {
      currentElement[name].push(element);
    } else {
      currentElement[name] = element;
    }
  } else {
    if (!currentElement[options.elementsKey]) {
      currentElement[options.elementsKey] = [];
    }
    element = {};
    element[options.typeKey] = 'element';
    element[options.nameKey] = name;
    element['isSelfClosing'] = isSelfClosing;
    if (
      !options.ignoreAttributes &&
      attributes &&
      Object.keys(attributes).length
    ) {
      element[options.attributesKey] = attributes;
    }
    if (options.alwaysChildren) {
      element[options.elementsKey] = [];
    }
    currentElement[options.elementsKey].push(element);
  }
  element[options.parentKey] = currentElement; // will be deleted in onEndElement() if !options.addParent
  currentElement = element;
}

function onText(text: any) {
  if (options.ignoreText) {
    return;
  }
  if (!text.trim() && !options.captureSpacesBetweenElements) {
    return;
  }
  if (options.trim) {
    text = text.trim();
  }
  if (options.nativeType) {
    text = nativeType(text);
  }
  if (options.sanitize) {
    text = text
      .replace(/&/g, '&amp;')
      .replace(/</g, '&lt;')
      .replace(/>/g, '&gt;');
  }
  addField('text', text);
}

function onComment(comment: any) {
  if (options.ignoreComment) {
    return;
  }
  if (options.trim) {
    comment = comment.trim();
  }
  addField('comment', comment);
}

function onEndElement(name: any) {
  var parentElement = currentElement[options.parentKey];
  if (!options.addParent) {
    delete currentElement[options.parentKey];
  }
  currentElement = parentElement;
}

function onCdata(cdata: any) {
  if (options.ignoreCdata) {
    return;
  }
  if (options.trim) {
    cdata = cdata.trim();
  }
  addField('cdata', cdata);
}

function onDoctype(doctype: any) {
  if (options.ignoreDoctype) {
    return;
  }
  doctype = doctype.replace(/^ /, '');
  if (options.trim) {
    doctype = doctype.trim();
  }
  addField('doctype', doctype);
}

function onError(error: any) {
  error.note = error; //console.error(error);
}


